// যোগ করতে চাইলে ( + ) সাইন ব্যবহার করবো

// উদাহরণ
console.log("_____যোগ_____");
console.log(5 + 2); // 7
console.log(10 + 7 + 25); // 42
console.log(10 + 9 + 11 + 25); // 55
console.log(80 + 62 + 36 + 41 + 511); // 730

let a = 5;
let b = 10;
let x = 25;
let y = 98;
console.log(a + b); // 15
console.log(a + b + x + y); // 138

// বিয়োগ করতে চাইলে ( - ) সাইন ব্যবহার করবো

// উদাহরণ
console.log("_____বিয়োগ_____");
console.log(5 - 2); // 3
console.log(25 - 10 - 3); // 12
console.log(98 - 50 - 25 - 10); // 13
console.log(511 - 41 - 36 - 15 - 10 - 2); // 407

let j = 5 - 2; // 3
let h = 25 - 10 - 3; // 12
let k = 98 - 50 - 25 - 10; // 13
let l = 511 - 41 - 36 - 15 - 10 - 2; // 407
console.log(j);
console.log(h);
console.log(k);
console.log(l);

// গুন করতে চাইলে ( * ) সাইন ব্যবহার করবো

