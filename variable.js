// String - কোনো ক্যারেক্টার লেখতে আমরা স্ট্রিং ব্যবহার করি
let firstName = "Ashraful";
let lastName = "Islam";
let fullName = firstName + " " + lastName;

console.log("Assalamuwalaikum," + " " + fullName);

// Number - কোনো নাম্বার নিতে চাইলে আমরা নাম্বার ডেটা টাইপ ব্যবহার করবো
let banana = 5;
let apple = 25;
let orange = 15;

console.log(banana);
console.log(apple);
console.log(orange);

// Boolean - বুলিয়ান সত্য বা মিথ্যা হবে।
let isExam = true;
let isLearner = true;
let isGirl = false;
console.log(isExam);
console.log(isLearner);
console.log(isGirl);

// Undefined - কোনো ভেরিএবল ডিক্লার করার পড়ে যদি ভেরিএবলে ভেলু না দেয় তাহলে সেটা undefined
let idNumber = undefined;
let age;
console.log(idNumber);
console.log(age);
